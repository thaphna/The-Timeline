/* hover peek: surface the event whose dot is directly under the cursor.
   Tracked on the stage (not the line) so the peeked dot doesn't steal
   the hover and flicker. Works for both the horizontal (desktop) and
   vertical (phone) line. */
const nodes = () => track.querySelectorAll('.node');
const isVertical = () => matchMedia('(max-width:600px)').matches;
function clearPeek(){
  track.querySelectorAll('.node.peek').forEach(n => n.classList.remove('peek'));
}
function findHit(clientX, clientY, slack){
  const rect = track.getBoundingClientRect();
  let along, cross, pos;
  if(isVertical()){
    along = clientY - rect.top;
    cross = clientX - (rect.left + rect.width / 2);
    pos = n => n.offsetTop;
  }else{
    along = clientX - rect.left;
    cross = clientY - (rect.top + rect.height / 2);
    pos = n => n.offsetLeft;
  }
  if(Math.abs(cross) > 18) return null;
  let hit = null;
  nodes().forEach(n => {
    if(Math.abs(pos(n) - along) <= slack) hit = n;
  });
  return hit;
}
stage.addEventListener('mousemove', e => {
  const hit = findHit(e.clientX, e.clientY, 9);  // the cursor must be on the dot itself
  clearPeek();
  if(hit) hit.classList.add('peek');
});
stage.addEventListener('mouseleave', clearPeek);

/* tap fallback: on touch screens there's no hover, so a tap on a dot's
   position along the line opens that event directly */
let suppressClose = false;
stage.addEventListener('click', e => {
  if(e.target.closest('.dot,.panel,.label')) return;
  const hit = findHit(e.clientX, e.clientY, 14);  // slightly wider for fingers
  if(hit){
    suppressClose = true;          // don't let the click-away handler undo this
    hit.querySelector('.dot').click();
  }
});

/* click-away: tapping empty space anywhere returns the line to normal */
function closeAll(){
  track.querySelectorAll('.node.open').forEach(n => {
    n.classList.remove('open');
    n.querySelector('.dot').setAttribute('aria-expanded','false');
  });
}

document.addEventListener('click', e => {
  if(suppressClose){ suppressClose = false; return; }
  if(e.target.closest('.dot,.panel,.label,.langs,.bigpic')) return;
  closeAll();
});
document.addEventListener('keydown', e => {
  if(e.key === 'Escape') closeAll();
});