function esc(s){
  return String(s ?? '').replace(/[&<>"']/g, c =>
    ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
}

function build(lang){
  const t = I18N[lang];
  document.documentElement.lang = lang;
  document.title = t.title + ' \u2014 The Timeline';
  document.getElementById('title').textContent = t.title;
  document.getElementById('summary').textContent = t.summary;
  hint.textContent = t.hint;

  track.querySelectorAll('.node').forEach(n => n.remove());

  t.events.forEach((ev, i) => {
    const [year, title, detail] = ev;
    const node = document.createElement('div');
    node.className = 'node ' + (i % 2 === 0 ? 'above' : 'below');
    node.style.setProperty('--p', (5 + i * (90 / (t.events.length - 1))) + '%');
    node.innerHTML =
      '<span class="stem"></span>' +
      '<button class="dot" aria-expanded="false" aria-label="' + esc(year + ' \u2014 ' + title) + '"></button>' +
      '<span class="label"><span class="year">' + esc(year) + '</span>' +
      '<span class="title">' + esc(title) + '</span></span>' +
      '<div class="panel top">' +
      '<div class="tbox"><div class="y">' + esc(year) + '</div>' +
      '<h3>' + esc(title) + '</h3></div>' +
      (IMAGES[i]
        ? '<div class="ibox">' +
          (IMAGE_LINKS[i]
            ? '<a class="imglink" href="' + esc(IMAGE_LINKS[i]) + '" target="_blank" rel="noopener" onclick="event.stopPropagation()">' +
              '<img class="photo" src="' + esc(IMAGES[i]) + '" alt="' + esc(title) + '" loading="lazy" onerror="this.closest(\'.ibox\').remove()"></a>'
            : '<img class="photo" src="' + esc(IMAGES[i]) + '" alt="' + esc(title) + '" loading="lazy" onerror="this.closest(\'.ibox\').remove()">') +
          '</div>'
        : '') +
      '</div>' +
      '<div class="panel bottom"><p>' + esc(detail) + '</p>' +
      (SOURCES[i]
        ? '<a class="more" href="' + esc(SOURCES[i]) + '" target="_blank" rel="noopener" ' +
          'onclick="event.stopPropagation()">' + esc(MORE[lang] || MORE.en) + '</a>'
        : '') +
      '</div>';
    const open = () => {
      const wasOpen = node.classList.contains('open');
      track.querySelectorAll('.node.open').forEach(n => {
        n.classList.remove('open');
        n.querySelector('.dot').setAttribute('aria-expanded','false');
      });
      if(!wasOpen){
        node.classList.add('open');
        node.querySelector('.dot').setAttribute('aria-expanded','true');
      }
    };
    node.querySelector('.dot').addEventListener('click', open);
    node.querySelector('.label').addEventListener('click', open);
    node.querySelectorAll('.panel').forEach(p => p.addEventListener('click', open));
    track.appendChild(node);
  });
}

